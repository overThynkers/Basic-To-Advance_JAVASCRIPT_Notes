//! INTRODUCTION TO WEBSITE (INTERVIEW QUESTIONS WITH ANSWERS)

//* How does a website work?
//? A website works when the browser sends a request to a web server and receives response files (HTML, CSS, JavaScript), which are then rendered on the screen.

//* What happens when a user enters a URL in the browser?
//? The browser sends an HTTP request to the server, the server processes it and returns the website files, which the browser displays.

//* What is a web server?
//? A web server is a computer/system that stores website files and delivers them to users when requested.

//* Which files are sent from server to browser?
//? HTML, CSS, and JavaScript files.

//* What is the role of browser in website rendering?
//? The browser reads HTML, CSS, and JavaScript and converts them into a visual webpage for the user.

//* What are the building blocks of a website?
//? HTML (structure), CSS (styling), JavaScript (functionality).

//* What is HTML and why is it used?
//? HTML is a markup language used to create the structure of a website.

//* What is CSS and what is its purpose?
//? CSS is used to style and design the website layout, colors, fonts, and spacing.

//* What is JavaScript and why is it important?
//? JavaScript is a programming language used to add interactivity and dynamic behavior to websites.


